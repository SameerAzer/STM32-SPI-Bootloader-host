int main(void)
{

  /* USER CODE BEGIN 1 */
  uint8_t version;
  // uint8_t sector_codes[2] = {1, 3};
  uint16_t chipID;
  uint32_t dummy = 0xDEADBEEF;
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();

  /* USER CODE BEGIN 2 */

  /* Synchronize with target bootloader ######################################*/
  BL_Init(&hspi1);

  /* Bootloader usage examples ###############################################*/
  BL_Get_Command(aRxBuffer);
  version = BL_GetVersion_Command();
  chipID = BL_GetID_Command();

  /* Check GetVersion is equal to GetCommand version */
  if (version != aRxBuffer[0])
  {
    Error_Handler();
  }

  /* Write dummy word to RAM */
  BL_WriteMemory_Command(0x20003000, 4, (uint8_t *) &dummy);

  /* Write chip ID to RAM */
  BL_WriteMemory_Command(0x20003004, 2, (uint8_t *) &chipID);

  /* Read dummy word and chip ID from RAM */
  BL_ReadMemory_Command(0x20003000, 6, aRxBuffer);

  /* Erase, write & verify test loop #########################################*/
  /* @note The Flash has to be erased before it can be written to */
  for (uint32_t i = 0; i < 5; i++)
  {

    /* Create dummy buffer using SysTick values */
    for (uint32_t j = 0; j <= 0xFF; j++)
    {
      aTxBuffer[j] = SysTick->VAL;
      /* Substitute NACK with 0xFF for easier debugging */
      if (aTxBuffer[j] == BL_NAK)
      {
        aTxBuffer[j] = 0xFF;
      }
    }

    /* (0x44) Erase Sector 0 (16 KBytes) */
    BL_EraseMemory_Command(0x0001, 0x00);

    /* (0x31) Write 255 Bytes dummy buffer */
    BL_WriteMemory_Command(0x08000000, 255, aTxBuffer);

    /* (0x11) Read memory */
    BL_ReadMemory_Command(0x08000000, 255, aRxBuffer);

    /* Verify received data */
    for (uint32_t k = 0; k < 0xFF; k++)
    {
      if (aTxBuffer[k] != aRxBuffer[k])
      {
        Error_Handler();
      }
    }
  }


  /* Jump to user application init vector ####################################*/
  BL_Go_Command(0x08000000);

  /* Security configuration examples #########################################*/
  /* @note The bootloader seems to become unresponsive after changing a option byte */
//
//  /* Write Protect Flash sectors */
//  BL_WriteProtect_Command(2, sector_codes);
//
//  /* Write Unprotect all Flash memory sectors*/
//  BL_WriteUnprotect_Command();
//
//  /* Reset Readout Protection to Level 0 (Flash mass erase) */
//  BL_ReadoutUnprotect_Command();
//
//  /* Set Readout Protection to Level 1 */
//  BL_ReadoutProtect_Command();


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_MSI;
  RCC_OscInitStruct.MSIState = RCC_MSI_ON;
  RCC_OscInitStruct.MSICalibrationValue = 0;
  RCC_OscInitStruct.MSIClockRange = RCC_MSIRANGE_6;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_NONE;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_MSI;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_0) != HAL_OK)
  {
    Error_Handler();
  }

    /**Configure the main internal regulator output voltage 
    */
  if (HAL_PWREx_ControlVoltageScaling(PWR_REGULATOR_VOLTAGE_SCALE1) != HAL_OK)
  {
    Error_Handler();
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

/* SPI1 init function */
static void MX_SPI1_Init(void)
{

  hspi1.Instance = SPI1;
  hspi1.Init.Mode = SPI_MODE_MASTER;
  hspi1.Init.Direction = SPI_DIRECTION_2LINES;
  hspi1.Init.DataSize = SPI_DATASIZE_8BIT;
  hspi1.Init.CLKPolarity = SPI_POLARITY_LOW;
  hspi1.Init.CLKPhase = SPI_PHASE_1EDGE;
  hspi1.Init.NSS = SPI_NSS_HARD_OUTPUT;
  hspi1.Init.BaudRatePrescaler = SPI_BAUDRATEPRESCALER_256;
  hspi1.Init.FirstBit = SPI_FIRSTBIT_MSB;
  hspi1.Init.TIMode = SPI_TIMODE_DISABLE;
  hspi1.Init.CRCCalculation = SPI_CRCCALCULATION_DISABLE;
  hspi1.Init.CRCPolynomial = 7;
  hspi1.Init.CRCLength = SPI_CRC_LENGTH_DATASIZE;
  hspi1.Init.NSSPMode = SPI_NSS_PULSE_DISABLE;
  if (HAL_SPI_Init(&hspi1) != HAL_OK)
  {
    Error_Handler();
  }

}

